#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		i;
	int		len;

	i = 0;
	len = ft_strlen(s1);
	if (!s1 || !set)
		return (NULL);
	while (s1[i] && ft_strchr(set, s1[i]))
		i++;
	while (len && ft_strchr(set, s1[len]))
		len--;
	return (ft_substr(&s1[i], 0, (len - i) + 1));
}

